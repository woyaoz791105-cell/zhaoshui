const BASE_SCORES = {
  I: 38,
  S: 38,
  E: 38,
  C: 38,
  R: 38,
  A: 38,
  X: 34,
  T: 30,
  D: 38,
  V: 38,
  P: 38,
  O: 38,
}

const scoresForCode = (code) => {
  const scores = { ...BASE_SCORES }

  code.split('').forEach((letter, index) => {
    scores[letter] = 96 - index * 8
  })

  return scores
}

